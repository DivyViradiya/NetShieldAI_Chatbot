# NOTE: The read_file_content_safely function has been removed, 
# as process_nmap_report_file now calls extract_text_from_pdf directly.

def process_nmap_report_file(file_path: str) -> Dict[str, Any]:
    """
    Handles file I/O for an Nmap report (assumed to be from a PDF report output): 
    checks existence, extracts raw text using the PDF extractor,
    calls the parser, and adds file system metadata.
    """
    if not os.path.exists(file_path):
        raise FileNotFoundError(f"Nmap report not found: {file_path}")
    print(f"Processing Nmap report: {file_path}")
    try:
        # --- CALLING THE PDF EXTRACTOR (Mirroring ZAP) ---
        raw_text = extract_text_from_pdf(file_path) 
        if not raw_text.strip():
            raise ValueError("Extracted text from file is empty.")
            
        report_data = parse_nmap_report(raw_text)
        
        report_data["file_metadata"] = {
            "filename": os.path.basename(file_path),
            "file_size": os.path.getsize(file_path),
            "last_modified": datetime.fromtimestamp(os.path.getmtime(file_path)).isoformat()
        }
        return report_data
        
    except Exception as e:
        print(f"Error processing Nmap report {file_path}: {e}")
        # Re-raise the exception to allow the calling app to handle the failure
        raise

